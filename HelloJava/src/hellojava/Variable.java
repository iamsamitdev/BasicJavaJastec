package hellojava;

public class Variable {
    public static void main(String[] args) {
        // การสร้างตัวแปรใน Java
        int anInt = 5;
        byte aByte = 15;
        long l = 2000009999998938933L;
        short aShort = 30233;
        char c = 'A';
        String s = "This is my string \"hi\" ";
        float f = 10.79F;
        double d = 29803803.55558788;
        boolean b = true;

        System.out.println(anInt);
        System.out.println(aByte);
        System.out.println(l+"\n"+aShort+"\n"+c+"\n"+s+"\n"+f+"\n"+d+"\n"+b);
    }
}
